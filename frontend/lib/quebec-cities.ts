/**
 * Normalize a string for lookup: lowercase + remove accents.
 */
function normalize(s: string): string {
  return s.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, "").trim();
}

/**
 * Look up coordinates for a Quebec city by name.
 * Returns null if not found — caller should fall back to text search.
 */
export function lookupCity(query: string): { lat: number; lng: number } | null {
  const q = normalize(query);
  // Direct match
  if (QUEBEC_CITIES[q]) return QUEBEC_CITIES[q];
  // Try with space↔hyphen variants
  const alt = q.includes(" ") ? q.replace(/ /g, "-") : q.replace(/-/g, " ");
  if (QUEBEC_CITIES[alt]) return QUEBEC_CITIES[alt];
  // Partial prefix match (e.g. "trois rivi" → "trois-rivieres")
  const match = Object.keys(QUEBEC_CITIES).find(k => k.startsWith(q) || q.startsWith(k));
  if (match) return QUEBEC_CITIES[match];
  return null;
}
